    private void loadData() {
        try {
            
            DefaultTableModel tbModel = (DefaultTableModel)tbPesanan.getModel(); 
            tbModel.setRowCount(0);
            
            cmd = Main.db.getConn().prepareStatement(
                "SELECT p.id, p.tgl_pesanan, p.jumlah, p.total_pembayaran, p.status, p.id_kulkas, k.nama FROM pesanan p " +
                "INNER JOIN kulkas k " +
                "ON (p.id_kulkas = k.id) " +
                (Main.username == "admin" ? "" : "WHERE username='"+Main.username+"'")
            );
            
            result = cmd.executeQuery();
            
            // masukkan data ke tabel
            while (result.next()) {
                String pesanan[] = {
                    result.getString("id"),
                    result.getString("tgl_pesanan"),
                    result.getString("nama") + " x " + result.getString("jumlah"),
                    result.getString("total_pembayaran"),
                    result.getString("status"),
                };
                
                tbModel.addRow(pesanan);
            }
            
            tbPesanan.addRowSelectionInterval(0, 0);
            
        } catch (Exception e) {
            Main.db.exceptionMsg(e);
            
        }
    }
    
