    private void tbPesananMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbPesananMouseClicked
        int row = tbPesanan.rowAtPoint(evt.getPoint());
        int col = tbPesanan.columnAtPoint(evt.getPoint());
        
        if (!(row >= 0 && col >= 0)) {
            return;
        } 

        try {
            cmd = Main.db.getConn().prepareStatement(
                    "SELECT * FROM pesanan " +
                            "WHERE id='" + tbPesanan.getValueAt(tbPesanan.getSelectedRow(), 0) + "'");
            result = cmd.executeQuery();

            Pesanan pesanan = new Pesanan();
            int idKulkas = 0;

            while (result.next()) {
                pesanan.setId(result.getString("id"));
                pesanan.setTglPesanan(result.getDate("tgl_pesanan"));
                pesanan.setJumlah(result.getInt("jumlah"));
                pesanan.setTotalPembayaran(result.getInt("total_pembayaran"));
                pesanan.setNamaPenerima(result.getString("nama_penerima"));
                pesanan.setAlamat(result.getString("alamat"));
                pesanan.setStatus(result.getString("status"));
                // pesanan.setIdKulkas(kulkas);
                Customer pemesan = new Customer();
                pemesan.setUsername(result.getString("username"));
                pesanan.setUsername(pemesan);
                

                idKulkas = result.getInt("id_kulkas");

            }

            cmd = Main.db.getConn().prepareStatement(
                    "SELECT * FROM kulkas " +
                            "WHERE id='" + Integer.toString(idKulkas) + "'");
            result = cmd.executeQuery();

            Kulkas kulkas = new Kulkas();
            while (result.next()) {
                kulkas.setId(result.getInt("id"));
                kulkas.setTglRilis(result.getDate("tgl_rilis"));
                kulkas.setNama(result.getString("nama"));
                kulkas.setHarga(result.getInt("harga"));
                kulkas.setMerk(result.getString("merk"));
                kulkas.setTipe(result.getString("tipe"));
                kulkas.setWarna(result.getString("warna"));
                kulkas.setDeskripsi(result.getString("deskripsi"));
                kulkas.setPanjang(result.getInt("panjang"));
                kulkas.setLebar(result.getInt("lebar"));
                kulkas.setTinggi(result.getInt("tinggi"));
                kulkas.setKapasitas(result.getInt("kapasitas"));
                kulkas.setStok(result.getInt("stok"));
                kulkas.setGambar(result.getBytes("gambar"));
            }
            pesanan.setIdKulkas(kulkas);

            FormPesanan struk = new FormPesanan();
            struk.setKulkas(kulkas);
            struk.setPesanan(pesanan);
            struk.setVisible(true);

        } catch (ArrayIndexOutOfBoundsException e) {

        } catch (Exception e) {
            Main.db.exceptionMsg(e);
        } // end try-catch

    }//GEN-LAST:event_tbPesananMouseClicked

